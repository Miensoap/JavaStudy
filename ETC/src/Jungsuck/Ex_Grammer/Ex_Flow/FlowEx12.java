public class FlowEx12 {
    public static void main(String[] args) {
        for (int i =1; i<=5;i++) {
            System.out.println(i);
        }
        for (int i=1; i<=5;i++){
            System.out.print(i); //print는 가로로
        }
        System.out.println();
    }


}
